'''
OPERATOR MATEMATIKA
+
/
*
-
** (pangkat)
% (modulo)
// (floor div)
'''
print(10 // 3)

# OPERATOR STRING
print("Fariz" + " " + "Fadila")

# OPERATOR BIT
# menggeser bit dari operand
print(9 << 2)
print(9 >> 2)

# PERANDINGAN
# < <= > >= == !=
print(245 == 245)
print(3 > 67)
print(53 != 644)

# BOOLEAN OPERATOR
# AND, OR, NOT, IN, NOT IN
print(True and False) # False 

# ASSIGNMENT OPERATOR
a = 35
a += 2
print(a)